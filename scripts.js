document.getElementById('fechar-box-id').addEventListener('click', function() {

    var contentDiv = document.getElementById('blackout-page-id');
    var contentDiv1 = document.getElementById('box-post-id');
    
    if (contentDiv.classList.contains('hidden')) {
        contentDiv.classList.remove('hidden');
    } else {
        contentDiv.classList.add('hidden');
    }

    if (contentDiv1.classList.contains('hidden')) {
        contentDiv1.classList.remove('hidden');
    } else {
        contentDiv1.classList.add('hidden');
    }
});

